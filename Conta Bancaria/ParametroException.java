
/**
 * Exception para sinalizar erros de parametros.
 * 
 * @author Erick de Oliveira Silva
 * @version 2017.10.17
 */
public class ParametroException extends Exception
{
	/**
	 * Construtor padrão.
	 *
	 * @param      msg   A mensagem da exceçao
	 */
	public ParametroException( String msg )
	{
		super( msg );
	}
}